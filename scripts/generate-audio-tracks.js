const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const audioDir = path.join(ROOT, 'frontend', 'public', 'audio');
const outFile = path.join(audioDir, 'tracks.js');

const AUDIO_EXTS = new Set(['.mp3', '.wav', '.ogg', '.m4a', '.flac', '.aac']);

function titleCase(input) {
  const cleaned = input
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
  if (!cleaned) return '';
  return cleaned
    .split(' ')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

function buildTracks() {
  if (!fs.existsSync(audioDir)) {
    throw new Error(`Audio folder not found: ${audioDir}`);
  }

  const files = fs
    .readdirSync(audioDir, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => AUDIO_EXTS.has(path.extname(name).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }));

  return files.map((file) => ({
    name: titleCase(path.basename(file, path.extname(file))),
    artist: 'local audio',
    src: `audio/${encodeURIComponent(file)}`
  }));
}

function writeTracks(tracks) {
  const header = '// Auto-generated by scripts/generate-audio-tracks.js. Do not edit by hand.\n';
  const body = `window.__MUSIC_TRACKS__ = ${JSON.stringify(tracks, null, 2)};\n`;
  fs.writeFileSync(outFile, header + body, 'utf8');
}

function generate() {
  const tracks = buildTracks();
  writeTracks(tracks);
  console.log(`Generated ${tracks.length} track(s) -> ${outFile}`);
}

const watchMode = process.argv.includes('--watch');

try {
  generate();
} catch (err) {
  console.error(err.message);
  process.exit(1);
}

if (watchMode) {
  console.log(`Watching ${audioDir} for changes...`);
  let timer;
  fs.watch(audioDir, () => {
    clearTimeout(timer);
    timer = setTimeout(() => {
      try {
        generate();
      } catch (err) {
        console.error(err.message);
      }
    }, 150);
  });
}
